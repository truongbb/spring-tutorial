package vn.com.ntqsolution;

public class Rule4 {

    /*
    *
    * ----------------------------4. Interface segregation principle----------------------------
    *
    *
    * RULE: Thay vì dùng 1 interface lớn, ta nên tách chúng thành nhiều interface nhỏ, với nhiều mục đích cụ thể
    *
    *
    * WHY: Điều này rất đơn giản, giả sử có 1 interface mà có cả trăm phương thức bên trong
    *       khi ta muốn implement interface đó ta phải cài đè tất cả các hàm đó ---> rất bất tiện
    *       chưa kể đến việc ta chỉ muốn dùng 1 hàm của interface đó mà thôi mà phải cài đè tất cả thì không hợp lí chút nào
    *
    *       --> Vậy nên, nên tách thành các interface nhỏ với các chức năng riêng biệt, 1 function càng tốt (functional interface)
    *
    *
    *
    *
    *
    * */

}
